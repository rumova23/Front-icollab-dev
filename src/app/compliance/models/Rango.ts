export class Rango {
    public max: string;
    public min: string;
    public color: string;
    public menssa: string;
 
    constructor(
        max: string,
        min: string,
        color: string,
        menssa: string,
    ) {
        this.max = max;
        this.min = min;
        this.color = color;
        this.menssa = menssa;
    }
}
